# DIAM(7) – propeller diameter knots (ft)
_DIAM = np.array([5.0, 6.5, 8.5, 11.0, 14.5, 18.0, 25.0])

# BBL(4) – blade numbers for 5-bladed special case
_BBL = np.array([2., 3., 4., 6.])


def znoise(BLADT: float, DIA: float, TIPSPD: float, VKTAS: float,
           BHP: float, DIST: float, FC: float, XNOE: float) -> Tuple[float, int]:
    """
    Exact translation of SUBROUTINE ZNOISE (NASA CR-2066)
    Computes A-weighted Sound Pressure Level (SPL) in dB.
    Returns (SPL, LIMIT) where LIMIT=0 is normal, !=0 means out-of-range.
    """
    # ===================================================================
    # Main computation (exact Fortran logic)
    # ===================================================================

    # Helical tip Mach number (TMT)
    TAS = VKTAS / 0.5925                     # knots → ft/s
    TMT = np.sqrt(TIPSPD**2 + TAS**2) / 1120.0 * FC

    # Blade family selection
    IB = int(BLADT - 1.0 + 0.001)

    if IB == 4:
        kk, nbb = 0, 4
    elif IB >= 5:
        kk, nbb = 3, 4
    else:
        kk = IB - 1
        nbb = kk + 1

    # Interpolate in the 3D PNLC table
    PNLA = np.zeros(7)
    PNLB = np.zeros(4)

    for K in range(kk, nbb):
        for I in range(7):
            PNLA[I], LIMIT = unint(13, _TMTH, _PNLC[:, I, K], TMT)
        PNLB[K], LIMIT = unint(7, _DIAM, PNLA, DIA)

    PNLD = PNLB[kk]
    if IB == 4:
        PNLD, LIMIT = unint(4, _BBL, PNLB, BLADT)

    # Final empirical SPL formula
    RMT      = TIPSPD / 1120.0
    LOG_BHP  = np.log(BHP)
    LOG_TERM = np.log(BLADT**2 * DIA**2 * DIST**2 / XNOE)

    SPL = (107.7 +
           6.69 * LOG_BHP -
           4.34 * LOG_TERM +
           38.1 * RMT +
           PNLD)

    if LIMIT != 0:
        SPL = 9999999.0

    return SPL, LIMIT