   20       FORMAT ('0',' NUMBER OF BLADES=',F3.0,18X' ACTIVITY FACTOR=',F4.0,18X' INTEGRATED DESIGN CL=',F4.3)
            IF(NCOST.EQ.1) GO TO 500
            GO TO (21,24),IW
   21       WRITE (6,22)
   22       FORMAT('0',' DIA.FT. T.S.FPS THRUST  PNL  ANGLE  FT   M  J   CP   CT'/)
            GO TO 30
   24       WRITE(6,25)
   25       FORMAT('0',' DIA.FT. T.S.FPS  SHP   PNL  ANGLE  FT   M  J   CP   CT'/)
            GO TO 30
  500       GO TO (510,550),IW			
  510       WRITE (6,520)
  520       FORMAT('0',30X,'*** 1970 TECHNOLOGY ***   *** 1980 TECHNOLOGY ***'/ ' DIA.FT. T.S.FPS THRUST  PNL  QUANTITY WT-LBS $COST  QUANTITY WT-LBS $COST  ANGLE  FT   M   J   CP   CT'/)
            GO TO 30
  550       WRITE (6,560)
  560       FORMAT('0',30X,'*** 1970 TECHNOLOGY ***   *** 1980 TECHNOLOGY ***'/ ' DIA.FT. T.S.FPS  SHP   PNL  QUANTITY WT-LBS $COST  QUANTITY WT-LBS $COST  ANGLE  FT   M   J   CP   CT'/)
   30     CONTINUE
          ILINE=ILINE+6
