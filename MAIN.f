              GO TO 720
  705         IF(ITS.EQ.1) GO TO 7000
              TIPSDG(ITS+1)=(ALOG(BHP(IC))-ALOG(BHPG(ITS-1)))* (TIPSDG(ITS)- TIPSDG(ITS-1))/(ALOG(BHPG(ITS))-ALOG(BHPG(ITS-1)))+TIPSDG(ITS-1)
              GO TO 709
 7000         TIPSDG(2)=400.
              TIPSPD=TIPSDG(ITS+1)
              GO TO 600
  712         THRSTG(ITS)=TIPSDG(ITS)**2*DIA**2*364.76*CT/(1.515E06*RORO(IC))
              IF (ABS(THRUST(IC)-THRSTG(ITS)).GE..005*THRUST(IC)) GO TO 722
              TIPSPD=TIPSDG(ITS)
              BHP(IC)=CP*2.0*TIPSPD**3*DIA**2/(10.E10*RORO(IC))*6966.
              TRIG=1.
              GO TO 720
  722         IF(ITS.EQ.1) GO TO 7000
              TIPSDG(ITS+1)=(ALOG(THRUST(IC))-ALOG(THRSTG(ITS-1)))* (TIPSDG(ITS)- TIPSDG(ITS-1))/(ALOG(THRSTG(ITS))-ALOG(THRSTG(ITS-1)))+TIPSDG(ITS-1)
  709         TIPSPD=TIPSDG(ITS+1)
              IF(NTS.NE.ITS) GO TO 600
              WRITE ( 6,598)
  598         FORMAT (//' FAILED STALL ITERATION '/)
              GO TO 700
																								C END OF TIPSPEED ITERATION 50 PERCENT STALL
																								C CALCULATION OF REQUIRED CP OR CT																																																
  399         IF(IW-1)400,400,430
  400         CP=BHP(IC)*10.E10*RORO(IC)/(2.0*TIPSPD**3*DIA**2*6966.)
              CALL PERFM (1,CP,ZJI,AFT,BLADT,CLI,CT,ZMS,LIMIT)
  420         THRUST(IC)=CT*TIPSPD**2*DIA**2/(1.515E06*RORO(IC))*364.76*XFT
              IF (CT.EQ.ASTERK) THRUST(IC)=9999999999.
              GO TO 460
  430         CT=THRUST(IC)*1.515E06*RORO(IC)/(TIPSPD**2*DIA**2*364.76)
              CALL PERFM (2,CP,ZJI,AFT,BLADT,CLI,CT,ZMS,LIMIT)
  450         BHP(IC)=CP*2.0*TIPSPD**3*DIA**2/(10.E10*RORO(IC))*6966.
              IF (CP.EQ.ASTERK) BHP(IC)=9999999999.
  460         IF (CP.NE.ASTERK) GO TO 720
              PNL=9999999999.
              WT70=999999.
              WT80=999999.
              COST70(1)=99999.
              COST80(1)=99999.
              GO TO 730
  720         PNL=0.0
              ISTALL=0
              IF(DIST(IC).LE.0.) GO TO 461
              CALL ZNOISE (BLADT,DIA,TIPSPD,VKTAS(IC),BHP(IC),DIST(IC),PNL,FC(IC),XNOE)
              CPA=CP
              CTA=CT
              SBLLL=BLLLL
              SXFT=XFT
              IWSV=IW
              IW=3
              CALL PERFM(3,CP,ZJI,AFT,BLADT,CLI,CT,ZMS,7710)
              CPS=CP
              CP=CPA
              CT=CTA
              BLLLL=SBLLL
              XFT=SXFT
              IW=IWSV
