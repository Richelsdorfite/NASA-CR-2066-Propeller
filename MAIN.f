              CALL COST (WTCON,BLADT,CLF1,CLF,CK70,CK80,CAMT,DAMT,NAMT,CQUAN(1,1),WT70,WT80,COST70,COST80,CCLF1,CCLF,CCK70,CCK80,IENT)
