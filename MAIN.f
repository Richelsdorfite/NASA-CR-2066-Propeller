  260   FORMAT(' ALT-FT =',F7.0,9X,'DESIGN FLIGHT M.=',F5.3,9X,'1000 FACTOR L.C.   =',F5.2/' V-KTAS =',F7.1,9X,'CLASSIFICATION =',F5.0,9X,'UNIT COST 1970   =',F5.1/' TEMP R =',F7.0,9X,'FIELD POINT FT. =',F5.0,9X,'UNIT COST 1980   =',F5.1)
