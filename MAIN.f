 2500 FORMAT (22X,'FULL THROTTLE SHP   =',F6.0/22X,'FULL THROTTLE RPM =',F6.0/22X,'TOUCH DOWN V-KNOTS =',F6.0/22X,'ALTITUDE FEET     =',F6.0/22X,'TEMPERATURE RANKINE=',F6.0//)
  270 DO 1200 IAF=1,NAF
        AFT=AFT+DAF
        IF(AFT.LE.200..AND.AFT.GE.80.) GO TO 182
        WRITE(6,181) AFT
  181   FORMAT(' ILLEGAL ACTIVITY FACTOR = ',F8.1)
        GO TO 1200
