              IF (STALIT(IC).LE..50) GO TO 399
              IWSV=IW
              IW=3
              CALL PERFM (3,CP,ZJI,AFT,BLADT,CLI,CT,ZMS,7710)
              IW=IWSV
              IF(IW.EQ.2) GO TO 712
  711         BHPG(ITS)=2.0*TIPSDG(ITS)**3*DIA**2*6966.*CP/(10.E10*RORO(IC))
              IF (ABS(BHP(IC)-BHPG(ITS)).GE..005*BHP(IC)) GO TO 705
              THRUST(IC)=CT*TIPSPD**2*DIA**2/(1.515E06*RORO(IC))*364.76
