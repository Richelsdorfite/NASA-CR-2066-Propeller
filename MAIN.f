  580         WRITE (6,575) DIA,TIPSPD,BHP(IC),PNL,CQUAN(1,1),WT70,COST70(1),CQUAN(2,1),WT80,COST80(1),BLLLL,XFT,ZM1,ZJI,CP,CT
  585         IF(NAMT-1) 40,40,586
  586         DO 588 I=2,NAMT
                WRITE(6,587) CQUAN(1,I),WT70,COST70(I),CQUAN(2,I),WT80,COST80(I)
  587           FORMAT (29X,2F8.0,F9.0,2F8.0,F9.0)
  588         CONTINUE
              GO TO 40
  730       GO TO (31,34),IW
   31       WRITE(6,32) DIA,TIPSPD,THRUST(IC),PNL,BLLLL,XFT,ZM1,ZJI,CP,CT
   32       FORMAT(F7.2,F7.0,F9.0,F6.0,F6.1,F8.3,F7.4,F8.3,2F8.4)
            GO TO 40
   34       WRITE(6,32) DIA,TIPSPD,BHP(IC),PNL,BLLLL,XFT,ZM1,ZJI,CP,CT
   40       IF(TRIG.EQ.1.) GO TO 750
            IF(ISTALL.EQ. 2) GO TO 800
            IF(IFIN.EQ.7710) GO TO 800
  600       CONTINUE
          IF (IW.LT.3) GO TO 750
																								C REVERSE THRUST CALCULATION
 3000     IRT=NPCPW(IC)
          PCPWC=PCPW(IC)
          DO 3900 I=1,IRT
            IF (RTC-1.) 3200,3100,3200
 3100       CP=BHP(IC)*PCPWC*RORO(IC)*10.E10/(2.0*RPMC(IC)**3*DIA**5*100.)
 3200       CALL REVTHT (RTC,ROT,AFT,CLI,BLADT,DIA,CP,BETA(IC),RORO(IC),BHP(IC),RPMC(IC),PCPWC,ANDVK(IC))
            PCPWC=PCPWC+DPCPW(IC)
 3900     CONTINUE
  750     CONTINUE
  800     CONTINUE
 1000     CONTINUE
 1001     CONTINUE
 1200   CONTINUE
  700 CONTINUE
      GO TO 701
